package com.utopia.core.exception;

public class OrganizationAccessException extends CoreException{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6571214787192042992L;

	public OrganizationAccessException() {
		super();
	}

	public OrganizationAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public OrganizationAccessException(String message) {
		super(message);
	}

	public OrganizationAccessException(Throwable cause) {
		super(cause);
	}

}
